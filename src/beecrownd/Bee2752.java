package beecrownd;

public class Bee2752 {
	public static void main(String[] args) {
		String f = "AMO FAZER EXERCICIO NO URI";
		
		System.out.printf("<%s>\n",f);
		System.out.printf("<%30s>\n",f);
		System.out.printf("<%.20s>\n",f);
		System.out.printf("<%-20s>\n",f);
		System.out.printf("<%-30s>\n",f);
		System.out.printf("<%.30s>\n",f);
		System.out.printf("<%30.20s>\n",f);
		System.out.printf("<%-30.20s>\n",f);
	}
}
